def load_app_config():
    """Loads application configuration from a local JSON file."""
    if os.path.exists(CONFIG_FILE):
        try:
            with open(CONFIG_FILE, "r") as f:
                config = json.load(f)
                app_config["active_model"] = config.get("active_model", app_config["active_model"])
                logger.info(f"Loaded active model from {CONFIG_FILE}: {app_config['active_model']}")
        except Exception as e:
            logger.error(f"Failed to load {CONFIG_FILE}: {str(e)}")

def save_app_config(model_name: str):
    """Saves application configuration to a local JSON file."""
    try:
        with open(CONFIG_FILE, "w") as f:
            json.dump({"active_model": model_name}, f)
            logger.info(f"Saved active model to {CONFIG_FILE}: {model_name}")
    except Exception as e:
        logger.error(f"Failed to save {CONFIG_FILE}: {str(e)}")

